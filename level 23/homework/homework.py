#5️ 5ამიხსენით list და indexing კომენატრებით 

# სიაში (list) თითოეულ ელემენტს აქვს თავისი ინდექსი, რომელიც იწყება 0-დან.
example_list = [10, 20, 30, 40, 50]

# ინდექსაცია:
# example_list[0] -> 10  (პირველი ელემენტი)
# example_list[1] -> 20  (მეორე ელემენტი)
# example_list[-1] -> 50 (ბოლო ელემენტი)
# example_list[1:4] -> [20, 30, 40] (მეორე, მესამე და მეოთხე ელემენტები)

print(example_list[1])  # დაბეჭდავს 20-ს
print(example_list[-1]) # დაბეჭდავს 50-ს
print(example_list[1:4]) # დაბეჭდავს [20, 30, 40]
